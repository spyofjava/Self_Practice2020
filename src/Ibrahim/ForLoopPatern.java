package Ibrahim;

public class ForLoopPatern {
    public static void main(String[] args) {

        for (int j=1;j<=10;j++){
            if (j==1){
                System.out.println("          * * * * * *");
            }else if (j==10) {
                System.out.println("          * * * * * *");
            }else {
                System.out.println("          *         *");
            }

        }

    }
}
