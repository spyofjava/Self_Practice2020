package Ibrahim;

import java.util.Scanner;

public class ForExercise {
    public static void main(String[] args) {
        Scanner x=new Scanner(System.in);
        int y=x.nextInt();
        int a=y;
        for(int i=y;i>0;i--){

            System.out.println(a);
            a=a-1;

        }




    }
}
